//Write a function that reverses a string. The input string is given as an array of characters char[].

// Example 1:
// Input: ["h","e","l","l","o"]
// Output: ["o","l","l","e","h"]

// Example 2:
// Input: ["H","a","n","n","a","h"]
// Output: ["h","a","n","n","a","H"]

let reverseString = function (s) {
  return s.reverse();
};

console.log(reverseString(["H", "a", "n", "n", "a", "h"]));
